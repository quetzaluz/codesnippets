def reverse(head):
    curr_node = head
    next_node = None
    prev_node = None
    if curr_node is None:
        prev_node = curr_node
    else:
        while curr_node is not None:
            next_node = curr_node.next
            curr_node.next = prev_node
            prev_node = curr_node
            curr_node = next_node
            if curr_node == head:
                break
    return prev_node

